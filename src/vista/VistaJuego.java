    public void apostar(){
        try{
            int valor = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese valor para apostar", "Apostar", JOptionPane.QUESTION_MESSAGE));
            if(valor > 0)controlador.apostar(valor);
            else mostrarError("Ingrese valor entero mayor a 0");
        }catch(NullPointerException ex){
            mostrarError("No se acepta vacío");
        }catch(NumberFormatException ex){
            mostrarError("Solo se aceptan números enteros mayor a cero");
        }catch(Exception ex){
            mostrarError("Exception no controlada");
        }
    }
    
    private void pasar() {
        this.lblEsperarNuevaMano.setText("Esperando nueva mano...");
        this.lblEsperarNuevaMano.setVisible(true);
        this.lblMostrarFondoMensajes.setVisible(true);
        this.btnApostar.setVisible(false);
        this.btnPasar.setVisible(false);
        controlador.pasar();
    }

    private void confirmarSalida(){
        int valor = JOptionPane.showConfirmDialog(this, "Está seguro que desea salir?", "Advertencia!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if(valor == JOptionPane.YES_OPTION){
            controlador.salirDelJuego();
            controlador.incrementarRespuesta();
            dispose();
        }
    }
    
